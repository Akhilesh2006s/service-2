export const getSkillSuggestions = (projectSkills: string[]) => {
  const allEmployees = mockEmployees;
  
  return allEmployees
    .map(employee => {
      const matchingSkills = employee.skills.filter(skill => 
        projectSkills.some(projectSkill => 
          skill.toLowerCase().includes(projectSkill.toLowerCase()) ||
          projectSkill.toLowerCase().includes(skill.toLowerCase())
        )
      );
      
      return {
        ...employee,
        matchingSkills,
        matchScore: matchingSkills.length
      };
    })
    .filter(employee => employee.matchScore > 0)
    .sort((a, b) => b.matchScore - a.matchScore)
    .slice(0, 5);
};

